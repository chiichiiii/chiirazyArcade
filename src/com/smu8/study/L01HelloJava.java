package com.smu8.study;

public class L01HelloJava {
    public static void main(String[] args) {
        // . 필드접근자 : 포함관계에 있는 자식을 접근 (System 하위 ==> 필드)
        // System: 메인을 실행하는 터미널(콘솔), out: 출력, print: 문자열 출력
        // ln == 라인개행 == Enter == "\n"
        System.out.println("안녕 첫 자바 수업~!!!!");
        System.out.print("프린트\n");
        System.out.print("프린트\n");
        System.out.print("프린트");
    }
}
