package com.smu8.homework;

public class H02Print {
    public static void main(String[] args) {
        System.out.print("문자열\n"); //String
        System.out.println('A'); //char
        System.out.println(7); //int
        System.out.println(1.1); //float
        System.out.println(7+1.1); //연산
        System.out.println(7.1*1.1); //연산
        System.out.println(0.1+0.2);
        System.out.printf("%.2f%n", 0.1+0.2);
    }
}
